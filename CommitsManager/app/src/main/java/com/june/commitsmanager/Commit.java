package com.june.commitsmanager;

public class Commit {
    String count;
    String lastCommit;
    String repository;
    String msg;


    public String getCount() {
        return count;
    }

    public String getLastCommit() {
        return lastCommit;
    }

    public String getRepository() {
        return repository;
    }

    public String getMsg() {
        return msg;
    }
}
